Decorator Pag 199:

El patrón decorator está diseñado para solucionar problemas donde la jerarquía 
con subclasificación no puede ser aplicada, o se requiere 
de un gran impacto en todas las clases de la jerarquía 
con el fin de poder lograr el comportamiento esperado. 
Decorator permite al usuario añadir nuevas funcionalidades a un objeto 
existente sin alterar su estructura, mediante la adición 
de nuevas clases que envuelven a la anterior dándole funcionamiento extra.

Cuándo utilizarlo:
 Cuando necesitamos agregar dinámicamente una nueva funcionalidad sobre un 
objeto y esta funcionalidad se agrega sobre capas de funcionalidad previas.
 Cuando implementar una nueva funcionalidad mediante la herencia es muy 
complicado o se requieren de fuertes cambios para implementarlos.